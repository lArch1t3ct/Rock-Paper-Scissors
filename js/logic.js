const CHOICES = ["rock", "paper", "scissors"];

function getComputerChoice() {
    const RANDOM_INDEX = Math.floor(Math.random() * 3);

    return CHOICES[RANDOM_INDEX];
}

const getHumanChoice = () => {
    let humanChoice;
    do {
        humanChoice = prompt("Rock Paper or Scissors?", "");
    } while (humanChoice !== "" && !CHOICES.includes(humanChoice.toLowerCase()));

    return humanChoice.toLowerCase();
};

function playGame() {
    let humanScore = 0;
    let computerScore = 0;

    const playRound = function (humanChoice, computerChoice) {
        if (humanChoice === computerChoice) {
            return "Draw!";
        } else if (humanChoice === "rock") {
            if(computerChoice === "paper"){
                computerScore++;
                return "Computer Won! Paper beats Rock";
            } else {
                humanScore++;
                return "Player Won! Rock beats Scissors";
            }
        } else if (humanChoice === "paper") {
            if( computerChoice === "scissors" ){
                computerScore++;
                return "Computer Won! Scissors Beats Paper";
            } else {
                humanScore++;
                return "Player Won! Paper beats Rock";
            }
        } else if (humanChoice === "scissors") {
            if( computerChoice === "rock" ){
                computerScore++;
                return "Computer Won! Rock beats Scissors";
            } else {
                humanScore++;
                return "Player Won! Scissors beats Paper";
            }
        }
    };

    console.log(playRound(getHumanChoice(), getComputerChoice()));
    console.log(playRound(getHumanChoice(), getComputerChoice()));
    console.log(playRound(getHumanChoice(), getComputerChoice()));
    console.log(playRound(getHumanChoice(), getComputerChoice()));
    console.log(playRound(getHumanChoice(), getComputerChoice()));

    if(humanScore > computerScore){
        console.log("Final winner is Player");
    } else if (computerScore > humanScore ){
        console.log("Final winner is Computer");
    } else {
        console.log("Both players have the same score. Draw!");
    }
}

playGame();